import { mkdirSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import { openuiChatLibrary, openuiChatPromptOptions } from "@openuidev/react-ui/genui-lib";

const __dirname = dirname(fileURLToPath(import.meta.url));
const outDir = join(__dirname, "src", "generated");
mkdirSync(outDir, { recursive: true });

/** Wrap a prompt string as a readable template literal. */
function toTemplateLiteral(s: string): string {
  const escaped = s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
  return "`" + escaped + "`";
}

// ── Chat prompt — no tools, pure markup generation ──────────────────────────
const chatPrompt = openuiChatLibrary.prompt(openuiChatPromptOptions);
writeFileSync(
  join(outDir, "system-prompt.ts"),
  `// Generated by \`pnpm generate\` — do not edit manually.
// Re-run after upgrading @openuidev/react-ui.
// prettier-ignore
export const SYSTEM_PROMPT = ${toTemplateLiteral(chatPrompt)};
`,
  "utf8",
);
console.log(`✓ Wrote system-prompt.ts (${chatPrompt.length} chars)`);

// ── App prompt — Query/Mutation enabled, tools_invoke injected ─────────────
const APP_PREAMBLE = `You are a single-page app generator. Your job is to produce a complete, runnable OpenUI Lang app that displays and interacts with real data.

You have access to the host system via the exec tool (through tools_invoke). The app you generate runs independently after creation — the runtime calls tools directly on every refresh with no LLM involved.

## Workflow

Follow these three steps in order. Do NOT skip straight to generating markup.

### Step 1: Discover data

Before writing any markup, explore what data is available by calling tools_invoke with tool_name "exec". Run commands to see what the system has:

  tools_invoke({tool_name: "exec", tool_args: {command: "vm_stat"}})
  tools_invoke({tool_name: "exec", tool_args: {command: "ps aux --sort=-%mem | head -10"}})
  tools_invoke({tool_name: "exec", tool_args: {command: "df -h"}})

Inspect the raw output — understand its format, fields, and what can be extracted.

### Step 2: Write a data script

Raw command output is rarely in a shape the UI can bind to directly. Write a self-contained script that:
- Calls the raw commands from step 1
- Parses and transforms the output into clean JSON
- Prints the JSON to stdout

The script should be a single command suitable for exec: a bash pipeline, \`node -e "..."\`, or \`python3 -c "..."\`.

Example — memory monitoring:
  Raw: vm_stat → messy text with "Pages active: 123456"
  Script: node -e "const os=require('os');console.log(JSON.stringify({totalGB:+(os.totalmem()/1e9).toFixed(1),freeGB:+(os.freemem()/1e9).toFixed(1),usedGB:+((os.totalmem()-os.freemem())/1e9).toFixed(1),pct:+((1-os.freemem()/os.totalmem())*100).toFixed(1)}))"
  Output: {"totalGB":16.0,"freeGB":2.1,"usedGB":13.9,"pct":86.9}

ALWAYS test the script by calling tools_invoke to verify it produces valid JSON before using it in the app.

### Step 3: Generate the app

Create the openui-lang app with Query() statements that call tools_invoke with your script from step 2:

  data = Query("tools_invoke", {tool_name: "exec", tool_args: {command: "...your script..."}}, {result: {defaults}}, refreshSeconds)

- The result is nested under \`.result\`: access fields as \`data.result.fieldName\`
- Use the real JSON from step 2 as condensed Query defaults (3rd arg) so the UI renders immediately
- For live monitors, pass a refresh interval as the 4th arg (e.g. 2 for every 2 seconds)
- For multiple data sources, create multiple Query() statements each with their own script
- NEVER hardcode data that a script can provide — everything must flow through Query()

The app runs independently after generation. The user sees it update in real time.`;

const appPrompt = openuiChatLibrary.prompt({
  ...openuiChatPromptOptions,
  toolCalls: true,
  bindings: true,
  editMode: true,
  inlineMode: true,
  preamble: APP_PREAMBLE,
  tools: [
    {
      name: "tools_invoke",
      description:
        "Invoke any available OpenClaw tool by name with the given arguments.",
      inputSchema: {
        type: "object",
        properties: {
          tool_name: {
            type: "string",
            description: "Name of the OpenClaw tool to invoke.",
          },
          tool_args: {
            type: "object",
            description: "Arguments to pass to the tool",
          },
        },
        required: ["tool_name", "tool_args"],
      },
      outputSchema: { type: "object", properties: { result: { type: "any" } } },
    },
  ],
});
writeFileSync(
  join(outDir, "app-system-prompt.ts"),
  `// Generated by \`pnpm generate\` — do not edit manually.
// Re-run after upgrading @openuidev/react-ui.
// prettier-ignore
export const APP_SYSTEM_PROMPT = ${toTemplateLiteral(appPrompt)};
`,
  "utf8",
);
console.log(`✓ Wrote app-system-prompt.ts (${appPrompt.length} chars)`);
